
// Subtask-1:

// Find all the odd numbers from 61 to 100.

let num1 = 61;
while (num1 <= 100) {
  if (num1 % 2 !== 0) {
    console.log('Odd Number:', num1);
  }
  num1 ++ ;
};


// Subtask-2:

// Find all the even numbers from 78 to 98.

let num = 78;
while (num <= 98) {
  if (num % 2 === 0) {
    console.log('Even Number:', num);
  }
  num ++ ;
};


